def add_numbers(a, b):
    """
    Function to add two numbers.

    Parameters:
    a (int or float): The first number.
    b (int or float): The second number.

    Returns:
    int or float: The sum of a and b.
    """
    return a + b

# Example usage:
result = add_numbers(5, 7)
print("Result:", result)  # Output: Result: 12